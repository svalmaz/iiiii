                <h3>Тема: Cветодиод</h3>
                <p><strong style="color: #d9534f;">Arduino Светодиод</strong> - бул Arduino негизиндеги долбоорлордо колдонулган жөнөкөй Светодиод. Бул жөнөкөй дисплей жана жарык эффекттерин түзүүгө мүмкүнчүлүк бергендиктен, Arduinoнун эң кеңири таралган компоненттеринин бири.</p>
                <p>Arduino Светодиод адатта Arduino тактасындагы санарип чыгарууга ток чектөөчү резисторду колдонуу менен туташтырылат. Светодиодго электрдик сигнал берилгенде ал жаркырай баштайт.</p>
                <p>Светодиод негизги мүнөздөмөлөрү болуп саналат:</p>
                <ul>
                    <li><strong style="color: #d9534f;">Өлчөмү:</strong> Бул кичинекей компакт өлчөмү бар жана адатта кичинекей тунук же түстүү диск катары көрүнөт.</li>
                    <li><strong style="color: #d9534f;">Пиндер:</strong> Светодиод эки буту бар - анод (узун буту) жана катод (кыска бут). Анод Arduino санариптик чыгышына туташтырылган, ал эми катод резистор аркылуу жерге туташтырылган.</li>
                    <li><strong style="color: #d9534f;">Көрсөтмө:</strong> Arduino Светодиод долбоордогу ар кандай абалдардын же окуялардын визуалдык көрсөткүчүн түзүү үчүн колдонулат. Мисалы, Arduino белгилүү бир аракетти аткарганда, же жарыктын же температуранын деңгээлин көрсөтсө, ал жыпылыктап турат.</li>
                </ul>
                <p>Arduino Светодиод ар кандай түстөрдө, анын ичинде кызыл, жашыл, көк, сары ж.б. бар, бул сизге Arduino негизиндеги долбоорлоруңузда ар кандай эффекттерди жана көрсөткүчтөрдү түзүүгө мүмкүнчүлүк берет.</p>
                <img src="lesson4-9.png" alt="Arduino менен LED туташуу диаграммасы">
                <h3>Arduino кодун талдап көрөлү</h3>
                <p>Келгиле, кодду биздин светодиот өчүп тургандай кылып өзгөртөлү. Код эки керектүү функцияны колдонот:</p>
                <ul>
                    <li><strong style="color: #d9534f;">setup()</strong> - функциясы эскиз башталганда чакырылат жана өзгөрмөлөрдү инициализациялоо, пиндердин иштөө режимдерин аныктоо, колдонулган китепканаларды ишке киргизүү ж.б. үчүн колдонулат. Орнотуу функциясы Arduino тактасын ар бир күйгүзгондон же баштапкы абалга келтиргенден кийин бир гана жолу иштейт.</li>
                    <li><strong style="color: #d9534f;">loop()</strong> - функциясы так өзүнүн аты айтып турган нерсени аткарып, программаңызга эсептөөлөрдү аткарууга жана аларга реакция кылууга мүмкүнчүлүк берет. Аны Arduino тактаңызды активдүү башкаруу үчүн колдонуңуз.</li>
                </ul>
                <p>setup() функциясынын ичинде жазылган функция бар:</p>
                <pre><code class="language-cpp">pinMode(pin, mode)</code></pre>
                <p>Бул функция көрсөтүлгөн киргизүү/чыгарма (пин) киргизүү же чыгаруу катары иштөө режимин орнотот.</p>
                <pre><code class="language-cpp">
pinMode(pin, mode);
// пин: сиз орноткуңуз келген киргизүү/чыгарма (пин) саны
// режим: режим эки маанинин бири болуп саналат - INPUT же OUTPUT, тиешелүү түрдө киргизүүнү же чыгарууну орнотот.
                </code></pre>
                <p>Чексиз цикл loop() эки функцияны камтыйт:</p>
                <ul>
                    <li><strong style="color: #d9534f;">digitalWrite()</strong> - функциясы санарип чыгууга (пин) ЖОГОРКУ же ТӨМӨНКҮ маанини берет. Эгерде чыгыш pinMode() функциясы тарабынан OUTPUT режимине коюлган болсо, анда HIGH мааниси үчүн тиешелүү чыгуудагы чыңалуу 5V, ал эми LOW үчүн 0V (жер) болот.</li>
                    <li><strong style="color: #d9534f;">delay()</strong> - функциясы параметрде көрсөтүлгөн миллисекунддардын санына (1 секундда 1000 миллисекунд) программанын аткарылышын токтотот.</li>
                </ul>
                <pre><code class="language-cpp">
digitalWrite(2, HIGH); // включаем светодиод
delay(500); // ждем 1000 millisecond(s) или 1 секунду
digitalWrite(2, LOW); // выключаем светодиод
delay(500); // ждем 1000 millisecond(s) или 1 секунду.
                </code></pre>
                <p>Биринчиден, биз LEDди күйгүзүп, андан кийин 1 секунд күтөбүз. Андан кийин биз LEDди өчүрүп, кайра Arduino'го чыңалуу колдонулганга чейин 1 секунд кутобуз.</p>
