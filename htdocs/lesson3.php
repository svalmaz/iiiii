                <h2>Тема №3: Arduino негизги түшүнүктөрү</h2>
                <p><strong style="color: #d9534f;">Arduino</strong> - Бул электрондук шаймандардын прототиптерин түзүү үчүн ачык аянтча. Ал аппараттык (микроконтроллерлер) жана программалык камсыздоону (интеграцияланган иштеп чыгуу чөйрөсү) камтыйт. Энтузиасттар, хоббисттер жана электроника адистери үчүн интерактивдүү долбоорлорду түзүүнүн оңой жана жеткиликтүү жолун камсыз кылат.</p>
                <p>Бул жерде сиз иштеп жатканда жолуга турган негизги түшүнүктөр:</p>
                <ul>
                    <li><strong style="color: #d9534f;">Плата Arduino:</strong> бул бүт система негизделген негизги элемент. Ал, адатта, микроконтроллерди, ар кандай киргизүү/чыгууларды (казыктарды), ошондой эле компьютерге жана башка түзмөктөргө туташуу үчүн интерфейстерди камтыйт.</li>
                    <img src="lesson4-1.png" alt="Плата Arduino">
                    <li><strong style="color: #d9534f;">Микроконтроллер:</strong> бул интегралдык микросхема, ал Arduinoнун мээси болуп саналат. Ал программанын кодун аткарууга жана туташкан түзмөктөрдү башкарууга жооп берет.</li>
                    <img src="lesson4-2.png" alt="Микроконтроллер">
                    <li><strong style="color: #d9534f;">Пиндер (порттор):</strong> булар тышкы түзмөктөрдү туташтыруу үчүн колдонулушу мүмкүн болгон Arduino платасындагы борттогу казыктар. Алар санариптик (экилик маалыматтарды берүү үчүн) же аналогдук (аналогдук сигналдарды берүү үчүн) болушу мүмкүн.</li>
                    <img src="lesson4-3.png" alt="Пиндер (порттор)">
                    <li><strong style="color: #d9534f;">Санариптик киргизүү/чыгаруу (Digital I/O):</strong> бул санариптик сигналдарды кабыл ала турган же өткөрө турган Arduino пиндери (0 же 1). Алар светодиоддорду, баскычтарды, релелерди жана башка санариптик шаймандарды туташтыруу жана башкаруу үчүн колдонулушу мүмкүн.</li>
                    <img src="lesson4-4.png" alt="Санариптик киргизүү/чыгаруу (Digital I/O)">
                    <li><strong style="color: #d9534f;">Аналогдук киргизүү/чыгаруу (Analog I/O):</strong> бул аналогдук сигналдарды кабыл ала турган же өткөрө турган пиндер. Алар сенсордук маалыматтарды окуу, светодиоддордун жарыктыгын же моторлордун ылдамдыгын башкаруу үчүн колдонулушу мүмкүн.</li>
                    <img src="lesson4-5.png" alt="Аналогдук киргизүү/чыгаруу (Analog I/O)">
                    <li><strong style="color: #d9534f;">Скетч (Sketch):</strong> бул Микроконтроллерге жүктөлгөн жана анын жүрүм-турумун аныктаган Arduino тилиндеги программа. Адатта, скетчтер автордук чөйрөдө жазылат жана инициализация үчүн <code>setup()</code> жана кодду аткаруунун негизги цикли үчүн <code>loop()</code> функцияларын камтыйт.</li>
                    <img src="lesson4-6.png" alt="Скетч (Sketch)">
                    <li><strong style="color: #d9534f;">Китепканалар:</strong> бул ар кандай түзмөктөр жана сенсорлор менен иштөөнү жеңилдетүү үчүн эскиздериңизде колдонула турган алдын ала жазылган функциялардын жана буйруктардын жыйындысы.</li>
